public class AccessToken {
    private String mAccessToken;
    private String mUserFull_Name;
    private String mUserId;
    private String mUserName;
    private String mProfilePicUrl;

    public String getmAccessToken() {
        return mAccessToken;
    }

    public void setmAccessToken(String mAccessToken) {
        this.mAccessToken = mAccessToken;
    }

    public String getmUserFull_Name() {
        return mUserFull_Name;
    }

    public void setmUserFull_Name(String mUserFull_Name) {
        this.mUserFull_Name = mUserFull_Name;
    }

    public String getmUserId() {
        return mUserId;
    }

    public void setmUserId(String mUserId) {
        this.mUserId = mUserId;
    }

    public String getmUserName() {
        return mUserName;
    }

    public void setmUserName(String mUserName) {
        this.mUserName = mUserName;
    }

    public String getmProfilePicUrl() {
        return mProfilePicUrl;
    }

    public void setmProfilePicUrl(String mProfilePicUrl) {
        this.mProfilePicUrl = mProfilePicUrl;
    }
}
